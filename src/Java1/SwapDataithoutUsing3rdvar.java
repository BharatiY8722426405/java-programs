package Java1;

public class SwapDataithoutUsing3rdvar {

	public static void main(String[] args) {
		int a=20;
		int b=10;
		System.out.println("Before Swap==>A value is="+a+" and B value is="+b);
		a=a+b;
		b=a-b;
		a=a-b;
		System.out.println("After Swap==>A value is="+a+" and B value is="+b);

		

	}

}
